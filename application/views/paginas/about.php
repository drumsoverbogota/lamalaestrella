
<p><h2>Acerca de</h2></b></p>

<p>
<em>El Muladar </em> es un archivo de Punk Colombiano que reune registros sonoros y audiovisuales desde los inicios del movimiento hasta el 2007.
</p>
<p>
Hace 6 años empezamos un proyecto llamado <em>Síntoma de última década </em> <a href="https://sintomadeultimadecada.wordpress.com">en forma de Blog</a>  con la idea de subir discos que no habían sido re-editados y que no estaban digitalizados, acompañados con algún pequeño escrito acerca del disco o como se obtuvo.
</p>

<p>
Con el tiempo hacer esto consumía mucho tiempo y no era tan fácil encontrar estos discos por lo cual fue abandonado. En esta página retomamos una idea similar (esto es, subir discos) pero ahora en una forma de archivo abierto al público en general.
</p>

<p>
Este es un proyecto autogestionado. El material acá presente se ha conseguido entre prestamos de amigos o amigos de amigos y partes de las (pequeñas) colecciones personales. Por tanto si usted, persona lectora de estas líneas, tiene algo que no se encuentre acá que quiera ora prestarnos (¡prometemos cuidarlo muy bien!) ora donarnos, <a href="<?php echo site_url('paginas/contact'); ?>">acá nos puede contactar</a>. 
</p>


<p>
Ningún derecho reservado, todo el material acá publicado es propiedad de sus compositor@s o autor@s. Por tanto,<b> no podemos dar la autorización de uso de nada de lo que está acá. Contacte a los autores o a sus representanets para obtener permiso.</b>
</p>

